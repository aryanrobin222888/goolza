/**
 * Reverse-lookup: find the slug for a given SofaScore tournament ID.
 */
export function getSlugById(tournamentId) {
  const id = String(tournamentId);
  for (const [slug, config] of Object.entries(TOURNAMENT_MAP)) {
    if (String(config.id) === id) return slug;
  }
  return null;
}

/**
 * Get tournament config by slug.
 */
export function getTournamentBySlug(slug) {
  return TOURNAMENT_MAP[slug] ?? null;
}
